window.addEventListener('load', function (){
  const headerBtn1 = document.getElementById("header-btn1");
  headerBtn1.addEventListener('mouseover', function(){
    headerBtn1.setAttribute("style", "color: tan")
  })
  headerBtn1.addEventListener('mouseout', function(){
    headerBtn1.removeAttribute("style", "color: tan")
  })

  const headerBtn2 = document.getElementById("header-btn2");
  headerBtn2.addEventListener('mouseover', function(){
    headerBtn2.setAttribute("style", "color: darkgray")
  })
  headerBtn2.addEventListener('mouseout', function(){
    headerBtn2.removeAttribute("style", "color: darkgray")
  })

  const headerBtn3 = document.getElementById("header-btn3");
  headerBtn3.addEventListener('mouseover', function(){
    headerBtn3.setAttribute("style", "color: red")
  })
  headerBtn3.addEventListener('mouseout', function(){
    headerBtn3.removeAttribute("style", "color: red")
  })
})

